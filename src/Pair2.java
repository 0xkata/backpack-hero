public class Pair2 {
    private int first;
    private int second;
    
    //constructor
    Pair2(int one, int two){
        first = one;
        second = two;
    }
    
    //getters
    public int getFirst() {
        return first;
    }
    public int getSecond() {
        return second;
    }
}